      <section id="ch2-8">
        <h3><span class="sub-chapter-numeral">2.8</span>What happened next?</h3>

        <p>This decision set a new national precedent for abortion regulations. The concept of “undue burden” became the test of whether proposed regulations would be allowed. But “undue burden” is a fairly vague phrase. How much burden is undue? Is the burden qualitative, meaning it affects each woman individually too much, or quantitative, meaning it affects too many women? Would it be a burden to regulate the procedure itself, or the qualifications of the woman seeking it, or the number of clinics allowed in a state? Over the following years, the Court struggled to find a consistent answer.</p>

