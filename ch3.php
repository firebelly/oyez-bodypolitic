      <section id="ch3-4">
        <h3><span class="sub-chapter-numeral">3.4</span>How did the Court change between the two Carhart cases?</h3>

        <p>Justice Sandra Day O’Connor retired from the bench in 2006 and was succeeded by the more conservative Justice Samuel Alito. Justice Alito served on the United States Court of Appeals for the Third Circuit when it heard Casey, before it reached the Supreme Court, and he had written the opinion upholding the abortion restrictions in question in that case. Justice Alito was predictably in favor of restrictions placed on abortion.</p>

      </section>

      <section id="ch3-5">
