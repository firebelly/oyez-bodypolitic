      <section id="ch3-1">
        <h3><span class="sub-chapter-numeral">3.1</span>What happened after Casey?</h3>

        <p>Not much changed on the national level after the Court decided Planned Parenthood v. Casey. Abortion was still legal, and states were still able to regulate the process. Although the Supreme Court had introduced the “undue burden” standard for what regulations were acceptable, it did not elaborate further on what an undue burden was.</p>

      </section>

      <section id="ch3-2">
        <h3><span class="sub-chapter-numeral">3.2</span>Who was Dr. Carhart?</h3>

        <p>Dr. LeRoy Carhart ran a clinic in Nebraska that offered many medical services, including late-term abortions. Dr. Carhart, like several similar doctors, had been a target of aggressive anti-abortion activism. In 1997, arsonists burned down his barn, killing 21 of his horses. The next day, Dr. Carhart received a letter that said his horses had been killed because he “killed babies.” He responded to the intimidation attempts by devoting his clinic entirely to women’s health, and continued to perform late-term abortions.</p>

      </section>

      <section id="ch3-3">
        <h3><span class="sub-chapter-numeral">3.3</span>What was Dr. Carhart’s first Supreme Court case?</h3>

        <p>In 1998, Dr. Carhart sued the state of Nebraska, challenging a law banning intact dilation and extraction (D&X), a common late-term abortion procedure. The procedure was referred to by some as “partial-birth abortion.” His case went to the Supreme Court as Stenberg v. Carhart (often referred to as Carhart I) in 2000. In this case, the Court ruled 5-4 that the Nebraska statute was unconstitutional under the standards set in Roe and Casey. Justice Stephen Breyer authored the opinion, which relied heavily on Dr. Carhart’s medical testimony that D&X was sometimes the safest method to terminate a late-term pregnancy and could be medically necessary. Justice Anthony Kennedy’s dissent criticized the majority’s reliance on Dr. Carhart’s expertise, and asserted that the majority opinion misrepresented the details of the D&X procedure.</p>

      </section>

      <section id="ch3-4">
        <h3><span class="sub-chapter-numeral">3.4</span>How did the Court change between the two Carhart cases?</h3>

        <p>Justice Sandra Day O’Connor retired from the bench in 2006 and was succeeded by the more conservative Justice Samuel Alito. Justice Alito served on the United States Court of Appeals for the Third Circuit when it heard Casey, before it reached the Supreme Court, and he had written the opinion upholding the abortion restrictions in question in that case. Justice Alito was predictably in favor of restrictions placed on abortion.</p>

      </section>

      <section id="ch3-5">
        <h3><span class="sub-chapter-numeral">3.5</span>What was the law in question, and what was the case against it?</h3>

        <p>In 2003, the federal government passed the Partial-Birth Abortion Ban Act. The act banned any procedure in which the death of the fetus occurred once “the entire fetal head or any part of the fetal trunk past the navel is outside the body of the mother.” This meant that the D&X procedure would be outlawed nationwide. Dr. Carhart again led the case against the ban, arguing that it was unconstitutional under Roe, Casey, and Carhart I. His case again made it to the Supreme Court.</p>

      </section>

      <section id="ch3-6">
        <h3><span class="sub-chapter-numeral">3.6</span>What happened in oral arguments?</h3>

        <p>A big issue was about the amount of deference that courts are supposed to give to Congressional findings regarding medical facts. If Congress says that the D&X procedure is never medically necessary, but some members of the medical community disagree, should courts defer to the findings of Congress or to the doctors? They also debated whether there was enough difference from Carhart I to rule differently. (Plus there was an unusual outburst from an observer in the courtroom.)</p>

      </section>

      <section id="ch3-7">
        <h3><span class="sub-chapter-numeral">3.7</span>What did the decision say?</h3>

        <p>Justice Anthony Kennedy wrote the majority opinion in the 5-4 decision. He stated that because the act only outlawed one major late-term abortion procedure, D&X, it did not create an undue burden on pregnant women seeking abortions. Justice Kennedy echoed some of his dissent from Carhart I, and he wrote that when the medical community had yet to determine the necessity of a procedure, the legislature was free to regulate it. The Court upheld “specific anatomical landmarks” in the statute. These landmarks were what separated the Nebraska statute in Carhart I from the federal statute in this case. For Kennedy, those distinctions mattered and separated D&X from other late-term abortion procedures.</p>

      </section>

      <section id="ch3-8">
        <h3><span class="sub-chapter-numeral">3.8</span>What did the dissent say?</h3>

        <p>Justice Ruth Bader Ginsburg wrote the fiery dissent, which was joined by Justices John Paul Stevens, David Souter, and Stephen Breyer. She wrote that upholding the Act violated the precedent set by Casey, and that the statute must include a provision regarding the pregnant woman’s health, which was absent. In announcing her dissent from the bench, she criticized the majority opinion as infringing on women’s rights.</p>

      </section>

      <section id="ch3-9">
        <h3><span class="sub-chapter-numeral">3.9</span>What happened next?</h3>

        <p>Overall, the decision sent a mixed message to the country about the Supreme Court’s position on abortion. The unique clash of morality, medicine, privacy, and women’s rights in each abortion case, plus different compositions of justices who heard each one, has led to different outcomes. The Court seemed willing to place limits on certain aspects of abortion, but would it overturn the right to abortion entirely? The answer remained unclear. In practical terms, only four doctors nationally perform late-term abortions today, a result of both the regulations and attempts on the lives of doctors who perform the procedure. </p>

      </section>

