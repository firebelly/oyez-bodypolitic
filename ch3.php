      <section id="ch3-9">
        <h3><span class="sub-chapter-numeral">3.9</span>What happened next?</h3>

        <p>Overall, the decision sent a mixed message to the country about the Supreme Court’s position on abortion. The unique clash of morality, medicine, privacy, and women’s rights in each abortion case, plus different compositions of justices who heard each one, has led to different outcomes. The Court seemed willing to place limits on certain aspects of abortion, but would it overturn the right to abortion entirely? The answer remained unclear. In practical terms, only four doctors nationally perform late-term abortions today, a result of both the regulations and attempts on the lives of doctors who perform the procedure. </p>

      </section>

      <section id="ch3-quicktake">
        <h4>Quick take:</h4>

        <ul>
          <li>The decisions in the Carhart cases showed a lack of teeth in their decision in <em>Planned Parenthood v. Casey</em>. In essence, “undue burden” was a standard that was proving difficult to define.</li>
          <li>One argument as to the change in doctrine between Casey and the Carhart cases - or even the difference between the Carhart cases - is that it was due more to the makeup of the Court rather than a shift in public opinion.</li>
