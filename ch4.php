        <h3><span class="sub-chapter-numeral">4.6</span>What could the decision say?</h3>

        <p>Check back after the decision is released for our analysis. Until then, here are some potential outcomes.</p>

        <ul>
          <li>An equally divided Court votes 4-4, and the decision of the lower court is affirmed. If this is the outcome, the law remains in place, but no national precedent is set.</li>
          <li>The justices hold the case over to next term for reargument, so that it can be heard by a full bench.</li>
          <li>The Court remands the case to a lower court for further arguments or fact-finding, which could have the effect of delaying the Court’s say on the matter until there are nine justices.</li>
          <li>The Court rules for the petitioners (the abortion clinics), and overturns or limits HB2. There is a wide range of possible outcomes in this instance.</li>
          <li>The Court rules for the respondents (Texas), and HB2 stays in place and similar laws across the country are permissible.</li>
        </ul>

