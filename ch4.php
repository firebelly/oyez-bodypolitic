        <h4>Quick take:</h4>
        <ul>
          <li>This is the first time a reproductive rights case has gone before a bench with three female justices. Most of the recent cases had had only one, while Justices Sandra Day O’Connor and Ruth Bader Ginsburg were both on the bench for <em>Carhart I</em>.</li>
          <li>A law similar to the Texas one was passed recently in Louisiana, and challengers appealed to the Supreme Court. After the Court heard arguments in <em>Whole Woman’s Health</em>, the justices put a stay on the Louisiana law and thus blocked its enforcement. While the Court’s inner workings are shrouded in mystery, this could be a sign that the Texas law may not stand as it is.</li>
