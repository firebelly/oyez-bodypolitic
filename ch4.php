        <h3><span class="sub-chapter-numeral">4.2</span>What was the law in question?</h3>

        <p>Texas House Bill 2, or HB2, received national attention when then-State Senator Wendy Davis filibustered it for 11 hours before it was ultimately passed by the Texas legislature and signed into law. HB2 contains two major provisions regarding abortion. The first requires physicians in Texas who perform abortions to have admitting privileges at a hospital within 30 miles of the abortion clinic. The second holds abortion clinics to the same facilities standard as ambulatory surgical centers. These provisions require a myriad of structural updates to many abortion clinics in the state. Supporters of the bill argue that HB2 is intended to protect women’s health.</p>

      </section>

      <section id="ch4-3">
        <h3><span class="sub-chapter-numeral">4.3</span>What was the case against the law?</h3>

        <p>The petitioners argue that while HB2 alleges to protect women’s health, the intended and actual effect of the bill is to limit women’s access to abortion across the state. Opponents say it would force 75% of clinics across the state of Texas to close, leaving thousands of square miles between San Antonio and the New Mexico border without any clinics. A pillar of their argument is that abortion is considered a relatively safe procedure, so these strict regulations are unnecessary for women’s health. Supporters of the bill respond by pointing to examples of unsafe abortion providers and the need to regulate for the protection of women.</p>

      </section>

      <section id="ch4-4">
        <h3><span class="sub-chapter-numeral">4.4</span>How has the Court changed since Carhart?</h3>

        <p>Since its decision in Carhart II, the Supreme Court has shifted. Although Justices John Paul Stevens and David Souter had been appointed by conservative presidents, they usually voted along more traditionally liberal or centrist lines. They each retired during President Obama’s tenure and were replaced by Justices Sonia Sotomayor and Elena Kagan, both consistent members of the Court’s liberal coalition.</p>

        <p>When the case was first granted, there were nine justices on the bench. But just before it was argued, Justice Antonin Scalia unexpectedly passed away at the age of 79. Justice Scalia was a well-known conservative voice on the bench who, in several cases, made clear that he did not view abortion as a constitutional right. His passing left eight justices on the bench: four liberals, three conservatives, and one swing vote - Justice Anthony Kennedy, who had voted to uphold the right to abortion in Casey, but had written the opinion restricting late-term abortions in Carhart II.</p>

      </section>

