      <section id="ch1-2">
        <h3><span class="sub-chapter-numeral">1.2</span>What was abortion law like then?</h3>

        <div class="aside-wrap">
          <p>At the time, abortion law was a complex national patchwork. Four states had repealed anti-abortion laws completely, and 13 states had reformed abortion laws to allow for certain circumstances beyond a danger to the woman’s health, known as the <span class="aside-trigger first-item" data-aside="ali-model"><span class="notice-me"><svg class="icon icon-info"><use xlink:href="#icon-info"></use></svg></span>ALI model</span> (after the American Law Institute, which proposed the model). The remaining 37 states had much stricter regulations on the procedure. Crossing state lines for an abortion was not uncommon for women who could afford such travel. In 1972, the year before the Court decided Roe, an estimated 100,000 women traveled to New York, the only state that did not have a residency requirement. Most women who could not afford the trip had to carry their pregnancy to term, but by some estimates, upwards of 200,000 women per year performed dangerous self-abortions.</p>
