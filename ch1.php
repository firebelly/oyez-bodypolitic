        <h3><span class="sub-chapter-numeral">1.1</span>Who was Roe?</h3>

        <p>In 1970, a Texas woman named Norma McCorvey sought to terminate her pregnancy, but was blocked by state law. This was McCorvey’s third pregnancy; her first child was raised by her mother, and her second child was put up for adoption. She found Linda Coffee and Sarah Weddington, two Texas lawyers who wanted to bring a suit challenging the Texas statute restricting abortion access. To protect her privacy in the case, she was recorded in court documents under the pseudonym Jane Roe.</p>
